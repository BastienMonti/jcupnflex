package fr.uha.hassenforder.jcupnflex.model;

/**
 * This class represents associativity for Terminal
 * 
 * NO    : not defined
 * LEFT  : left associative operator
 * RIGHT : right associative operator
 * NONE  : no associative operator
 *
 * @version last updated: 28/09/2022
 * @author Michel Hassenforder
 */

public enum Associativity {
	NO,
	LEFT,
	RIGHT,
	NONE,
}
